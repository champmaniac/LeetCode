class Solution
{
    public:
    long long countKdivPairs(int A[], int n, int K)
    {
        //code here
        unordered_map<long long,long long> mp;
        long long i, cnt=0;
        for(i=0;i<n;i++){
            long long rem = A[i]%K;
            if(rem==0) cnt+=mp[0];
            else cnt+=mp[K-rem];
            mp[rem]++;
        }
        return cnt;
    }
};

